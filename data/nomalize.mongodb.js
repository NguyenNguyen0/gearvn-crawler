function normalizeKey(key) {
    return key
        .normalize("NFD")
        .replace(/[\u0300-\u036f]/g, "")
        .replace(/đ/g, "d").replace(/Đ/g, "D")
        .toLowerCase()
        .replace(/\s+/g, "_");
}

const unmapped = new Set();

db.products.find().forEach(doc => {
    if (!doc.specs) return;

    const renames = {};
    const additions = {};
    const seen = new Set();

    Object.entries(doc.specs).forEach(([rawKey, value]) => {
        const normalized = normalizeKey(rawKey);
        const mappedKey = specMap[normalized];

        if (mappedKey) {
            if (rawKey === mappedKey) return;
            renames[rawKey] = null;
            if (!seen.has(mappedKey)) {
                additions[mappedKey] = value;
                seen.add(mappedKey);
            }
        } else {
            unmapped.add(normalized); // dùng normalized để dễ add vào specMap sau
        }
    });

    if (Object.keys(renames).length === 0) return;

    const unsetFields = {};
    Object.keys(renames).forEach(k => { unsetFields[`specs.${k}`] = ""; });

    const setFields = {};
    Object.entries(additions).forEach(([k, v]) => { setFields[`specs.${k}`] = v; });

    db.products.updateOne(
        { _id: doc._id },
        { $unset: unsetFields, $set: setFields }
    );
});

